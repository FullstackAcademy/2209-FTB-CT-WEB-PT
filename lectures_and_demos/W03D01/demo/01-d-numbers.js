/* 
NUMBERS
OPERATORS
*/
console.log(typeof 4.5); // type
console.log(4 + 3) // addition
console.log(4 - 3) // subtraction
console.log(4 * 3) // multiplication
console.log(4 / 3) // division
console.log(4 % 3) // mod
console.log(4 ** 3) // exponents

/*
ORDER OF OPERATIONS - PEMDAS 
P - Parentheses
E - Exponents
MD - Multiplication, Division
AS - Addition, Subtraction
*/
console.log(1 + 2 * 7 ** 2);
console.log(((1 + 2) * 7) ** 2); // parentheses
console.log(((-1 + 2) * 7) ** 2); // negatives
console.log(((-1 + 2) * 7.5) ** 2); // floats - Also part of "number" type
